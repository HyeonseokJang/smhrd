
public class Sensor {
private int sensor1;
private int sensor2;
public Sensor() {}
public Sensor(int sensor1, int sensor2) {
	
	this.sensor1 = sensor1;
	this.sensor2 = sensor2;
}
public int getSensor1() {
	return sensor1;
}
public void setSensor1(int sensor1) {
	this.sensor1 = sensor1;
}
public int getSensor2() {
	return sensor2;
}
public void setSensor2(int sensor2) {
	this.sensor2 = sensor2;
}




}
